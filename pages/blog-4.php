tion, our family doctor never would have referred us to a local paediatrician (who goes above and beyond any health professional I've ever met. Amazing dude), and we never would have made it to our newest normal with Isla. </p>
                        
                        <p class="text-faded">Ok, I'm finally going to tell you. Hopefully the suspense is unbearable now for you, my lovely readers, who do not know our family personally (I know, leading you on like this was a dick move, but we have made it). It was mid December 2015. Isla was just over a month old. I was working in the city and Katie was in the midst of her 4 months maternity leave (we decided I'd take the remaining 8 months, hence the term Fommy. Best decision of my life). It was the day Katie brought Isla in to see her paediatrician. It was about 6:30 pm when I got home from work and Katie had waited for me to arrive home before telling me how the doctor’s visit went. I remember walking in the door and seeing Katie holding our sweet baby girl tightly with tears in her eyes. She said the meeting did not go so well. Our whole lives we have been extremely fortunate so I always assume the best outcome, which makes it that much harder to hear bad news for once. However, we knew someday it was bound to happen. The paediatrician agreed that the jaundice and pale coloured poop was concerning. He advised Katie to head to the Valley Regional Hospital that day and get blood work done. He told us that there could be several reasons for these symptoms, but he wanted to rule out the most serious one, which is a dandy little disease called Biliary Atresia. This is where we learned that our paediatrician was a god send and he takes his career to a whole new level of awesomeness. He called Katie after hours to share with her the abnormal blood results and he called multiple times afterwards to ensure we were doing ok as well as having all of our questions answered. Our doctor presumed, with the risk of Biliary Atresia, that we would be admitted into the IWK Children's Hospital in Halifax. Unfortunately, he was correct and we took a trip to the hospital a couple days after the blood was drawn. </p>
                        
