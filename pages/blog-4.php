                        <p class="text-faded">Flash back again to a few days prior to the day we were ready to enforce the return policy on Isla. We were at the hospital for our final day, full of anticipation to get the "all clear" for her routine tests and head home. Everything is how it should be. She is responsive, her belly pokes reveal normality, and she even had that disgusting amniotic poop (the one that looks like molasses (knew there was a reason I didn't trust molasses). The only concern they had was her bilirubin levels were on the high side of normal (hence the extra day at the hospital). For those of you who don’t know, bilirubin is the active part of your bile, which is secreted from your liver and into your small intestine. It’s main function is the break down of red blood cells and digestion of fat in your diet (by no means is that the medical definition, but it is my understanding. There will be more laymen's definitions to come as we have had many’a learnings over the last few months). That's fairly normal though and even if her levels didn't drop they would just give her her first trip to the tanning bed and say goodbye to her newborn jaundice. Long story short, the bilirubin levels did drop and we were released that day. We bundled her up in her car seat like an easily bruised kiwi, carried her out to the SUV like she would shatter if dropped, and headed home to our new normal! </p> 
    
